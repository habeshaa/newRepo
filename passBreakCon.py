# pass  Does nothing — it’s just a placeholder.
print("pass in py")
for i in range(5):
    if i==3:
        pass
    print(i)

# break   Stops the entire loop immediately.   Used when you want to exit a loop early
print("break in py")
for i in range(5):
    if i==3:
        break
    print(i)

# continue  Skips the current iteration and moves to the next one.

print("continue in py")
for i in range(5):
    if i==3:
        continue
    print(i)

